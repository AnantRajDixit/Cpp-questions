//Take two numbers and print sum, difference, product, division.
#include<iostream>
using namespace std;
int main(){
    int a;
    int b;
    cout<<"Enter the first number"<<endl;
    cin>>a;
    cout<<"Enter the second number"<<endl;
    cin>>b;

    cout<<"sum of two number is : "<<a+b<<endl;
    cout<<"Differnce between the two number is: "<<a-b<<endl;
    cout<<"Product of the two number is: "<<a*b<<endl;
    cout<<"Division of the two number is: "<<a/b<<endl;
    
    return 0;
    
}