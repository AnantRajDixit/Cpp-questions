//Find area of rectangle.
#include<iostream>
using namespace std;

int main(){
    double length;
    double breadth;
    cout<<"Enter the length: ";
    cin>>length;
    cout<<"Enter the breadth: ";
    cin>>breadth;
    double area = length*breadth;
    cout<<"The area of the rectangle is: "<<area<<endl;

    return 0;
}
